export function close(element) {
    return element.close();
}

export function closeOpened(element) {
    return element.closeOpened();
}

export function getOpenAmount(element) {
    return element.getOpenAmount();
}

export function getSlidingRatio(element) {
    return element.getSlidingRatio();
}

export function open(element, side) {
    return element.open(side);
}
