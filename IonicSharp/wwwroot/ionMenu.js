export function close(element, animated) {
    return element.close(animated);
}

export function isActive(element) {
    return element.isActive();
}

export function isOpen(element) {
    return element.isOpen();
}

export function open(element, animated) {
    return element.open(animated);
}

export function setOpen(element, shouldOpen, animated) {
    return element.setOpen(shouldOpen, animated);
}

export function toggle(element, animated) {
    return element.toggle(animated);
}
